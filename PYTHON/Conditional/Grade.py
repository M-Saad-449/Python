if Percentage>=80:
    print("The student has A+ Grade")
elif Percentage>=70:
    print("The student has A Grade")
elif Percentage>=60:
    print("The student has B Grade")
elif Percentage>=70:
    print("The student has C Grade")
elif Percentage>=40:
    print("The student has D Grade")
