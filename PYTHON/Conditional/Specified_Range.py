Number = int(input("Enter a number : "))
Starting_Number  = int(input("Enter the starting number : "))
Ending_Number = int(input("Enter the ending number : "))
if (Number>=Starting_Number) and (Number<=Ending_Number):
    print("The given number ",Number,"is in a specified range")
else:
    print("The give number ",Number, "is not in specified range")